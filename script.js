const canvas = document.getElementById("vizCanvas");
const ctx = canvas.getContext("2d", { willReadFrequently: true });
const uploadInput = document.getElementById("upload");
const brushInput = document.getElementById("brush");
const resetMaskBtn = document.getElementById("resetMask");
const swatches = [...document.querySelectorAll(".swatch")];

let img = new Image();
let maskCanvas = document.createElement("canvas");
let maskCtx = maskCanvas.getContext("2d");
let painting = false;
let brushSize = Number(brushInput.value);
let activeColor = swatches[0].dataset.color;

function drawDefault() {
  ctx.fillStyle = "#121212";
  ctx.fillRect(0, 0, canvas.width, canvas.height);
  ctx.fillStyle = "#ffffff";
  ctx.font = "24px Segoe UI";
  ctx.fillText("Carregue uma imagem para começar a simulação.", 40, 70);
}

function fitImageToCanvas(image) {
  const ratio = Math.min(canvas.width / image.width, canvas.height / image.height);
  const w = image.width * ratio;
  const h = image.height * ratio;
  const x = (canvas.width - w) / 2;
  const y = (canvas.height - h) / 2;
  return { x, y, w, h };
}

function render() {
  if (!img.src) {
    drawDefault();
    return;
  }

  ctx.clearRect(0, 0, canvas.width, canvas.height);
  const box = fitImageToCanvas(img);
  ctx.drawImage(img, box.x, box.y, box.w, box.h);

  const base = ctx.getImageData(0, 0, canvas.width, canvas.height);
  const mask = maskCtx.getImageData(0, 0, canvas.width, canvas.height);
  const data = base.data;
  const m = mask.data;
  const rgb = hexToRgb(activeColor);

  for (let i = 0; i < data.length; i += 4) {
    const alpha = m[i + 3] / 255;
    if (alpha < 0.03) continue;
    const lum = (data[i] + data[i + 1] + data[i + 2]) / 3;
    const texture = (Math.sin(i * 0.0002) + 1) * 0.5;
    const blend = 0.42 + texture * 0.12;
    data[i] = data[i] * (1 - blend * alpha) + rgb.r * blend * alpha + lum * 0.03;
    data[i + 1] = data[i + 1] * (1 - blend * alpha) + rgb.g * blend * alpha + lum * 0.03;
    data[i + 2] = data[i + 2] * (1 - blend * alpha) + rgb.b * blend * alpha + lum * 0.03;
  }
  ctx.putImageData(base, 0, 0);
}

function pointerPos(evt) {
  const rect = canvas.getBoundingClientRect();
  const scaleX = canvas.width / rect.width;
  const scaleY = canvas.height / rect.height;
  return {
    x: (evt.clientX - rect.left) * scaleX,
    y: (evt.clientY - rect.top) * scaleY
  };
}

function paint(evt) {
  if (!painting) return;
  const p = pointerPos(evt);
  maskCtx.fillStyle = "#ffffff";
  maskCtx.beginPath();
  maskCtx.arc(p.x, p.y, brushSize, 0, Math.PI * 2);
  maskCtx.fill();
  render();
}

canvas.addEventListener("pointerdown", (evt) => {
  painting = true;
  paint(evt);
});
canvas.addEventListener("pointermove", paint);
canvas.addEventListener("pointerup", () => (painting = false));
canvas.addEventListener("pointerleave", () => (painting = false));

brushInput.addEventListener("input", () => {
  brushSize = Number(brushInput.value);
});

resetMaskBtn.addEventListener("click", () => {
  maskCtx.clearRect(0, 0, canvas.width, canvas.height);
  render();
});

swatches.forEach((btn) =>
  btn.addEventListener("click", () => {
    activeColor = btn.dataset.color;
    swatches.forEach((b) => b.classList.remove("active"));
    btn.classList.add("active");
    render();
  })
);

uploadInput.addEventListener("change", (evt) => {
  const file = evt.target.files?.[0];
  if (!file) return;
  const url = URL.createObjectURL(file);
  img = new Image();
  img.onload = () => {
    maskCanvas.width = canvas.width;
    maskCanvas.height = canvas.height;
    maskCtx.clearRect(0, 0, canvas.width, canvas.height);
    render();
    URL.revokeObjectURL(url);
  };
  img.src = url;
});

function hexToRgb(hex) {
  const normalized = hex.replace("#", "");
  const int = parseInt(normalized, 16);
  return {
    r: (int >> 16) & 255,
    g: (int >> 8) & 255,
    b: int & 255
  };
}

